def astock_decision_prompt(symbol, thesis="", horizon="中线"):
    return f"""
A股代码：{symbol}
投资周期：{horizon}
核心假设：{thesis or "请填写产业逻辑、公司位置、财务验证和价格结构。"}

请按 TradingAgents-AShare + Vibe-Trading 风格完成：
1. 分析师团队：基本面、情绪、新闻、技术、宏观/政策、主力资金、解禁减持。
2. 多方研究员：为什么可以买、持有或继续跟踪。
3. 空方研究员：最可能证伪的证据和反身性风险。
4. 研究总监：合并多空证据，判断当前阶段和主要矛盾。
5. 交易员：把结论转成A股约束下的可执行计划，考虑T+1、涨跌停、手数、ST/退市和交易时段。
6. 风控辩论：激进、稳健、中性三种仓位意见。
7. 影子账户复盘：如果这笔交易来自真实持仓，检查是否存在冲动交易、亏损加仓、盈利过早卖出、概念跟涨或忽视流动性。
8. 组合经理：输出结论、置信度、仓位上限、目标价、止损/失效位、核心风险、下一步跟踪数据。
""".strip()
